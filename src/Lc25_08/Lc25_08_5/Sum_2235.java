package Lc25_08.Lc25_08_5;

// [25.7.31] 两整数相加
public class Sum_2235 {
    public static void main(String[] args) {
        System.out.println(sum(12,5));  // 输出 17
        System.out.println(sum(-10,4)); // 输出 -6
    }
    public static int sum(int num1, int num2) {
        return num1 + num2;
    }
}
